    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed

        //registrar marcas
        if (validar()) {
            rajus.agregar_motivo_Ajuste(text_motivo.getText(), comboMotivo.getSelectedItem().toString());

            JOptionPane.showMessageDialog(this, "Datos Registrados");
            fill_table();
            lblCont.setText(rajus.count_motivos_ajuste() + "");

        }

    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        // TODO add your handling code here:
        if (validar()) {
            int seleccion = JOptionPane.showOptionDialog(null, "Desea Actualizar la Motivo",
                    "  ", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                    new Object[]{"Si    ", "   No",}, "Si");
            if (seleccion == JOptionPane.YES_OPTION) {
                rajus.editar_motivo_Ajuste(Integer.parseInt(id_motivo), text_motivo.getText(), comboMotivo.getSelectedItem().toString());
                JOptionPane.showMessageDialog(this, "Datos Actualizados");
                fill_table();
            }

        }
    }//GEN-LAST:event_btn_actualizarActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        if (validar()) {
            int seleccion = JOptionPane.showOptionDialog(null, "Desea Eliminar Motivo",
                    "  ", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                    new Object[]{"Si    ", "   No",}, "Si");
            if (seleccion == JOptionPane.YES_OPTION) {

                rajus.eliminar_motivo_Ajuste(Integer.parseInt(id_motivo));

                fill_table();
                lblCont.setText(rajus.count_motivos_ajuste() + "");
            }

            /* label_men.setForeground(Color.GREEN);
             label_men.setText("Datos Eliminados Correctamente");*/
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

