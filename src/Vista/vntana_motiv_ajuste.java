    private void tbl_marcaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_marcaMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            id_motivo = tbl_marca.getValueAt(tbl_marca.getSelectedRow(), 0).toString();
            text_motivo.setText(tbl_marca.getValueAt(tbl_marca.getSelectedRow(), 1).toString());
            comboMotivo.setSelectedItem(tbl_marca.getValueAt(tbl_marca.getSelectedRow(), 2).toString());
            pinta_text();
            campos(true);
            if (delet == true && update == true) {
                botones(false, true);
            } else {
                actBotones(update, delet);
            }
        }
    }//GEN-LAST:event_tbl_marcaMouseClicked

